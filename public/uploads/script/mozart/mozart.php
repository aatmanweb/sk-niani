<p>Just imagine how the world would be indeed without such beauty to please the eye and calm the soul. Nature’s allure takes various forms, one of which is music. Across the emerald forests and aquamarine waters, the language of music remains the same, irrespective of caste, creed and nationality. Music possesses an irresistible power to stir hearts and invoke an immediate reaction. Almost every one of us has a favourite tune which we never tire of and which always manages to make us feel good. Such potency; it’s almost like a weapon; blessed are the men and women who know how to create it.</p>

<p>Over the aeons, the world has seen countless composers; their music has brought forth delightful smiles and laughter, sweet pain and also soothing calm. One of the greatest composers is a man called Wolfgang Amadeus Mozart. The love affair of Mozart and Music has given the world, something to suit every mood; delicate, light fairy like compositions; serene, restful harmonies and stormy, strong, powerful music. Einstein once said that while Beethoven created his music, Mozart’s “was so pure that it seemed to have been ever-present in the universe, waiting to be discovered by the master.” What was the secret behind such effortless mastery? How do his compositions seem ever fresh? Let’s begin on a musical journey and peep into the life of a maestro whose tunes refuse to be blotted from our minds even after 200 years.</p>

