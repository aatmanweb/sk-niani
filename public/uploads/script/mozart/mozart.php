<p>Mannheim transformed Wolfgang’s life in many ways. For the first time in his adult life, he was working in such a musically charged atmosphere. Leopold himself called Mannheim <strong>“that famous court, whose rays are like the sun’s, lighting up the whole of Germany.”</strong> But he lambasted Wolfgang for stopping at Mannheim without consulting him first, instead of directly heading for Paris. The shrewd Leopold knew that in a place brimming with talent like Mannheim, Wolfgang would be welcomed only as a visitor. The father son relationship was developing cracks; the closeness which they had always shared and which had reached its peak during their trips to Italy was starting to wither.</p>

<p><span id="A natural death or murder? ">Leopold bombarded Wolfgang with letters which were always chock-a-block with advice bordering on orders; Wolfgang must keep to his diet, his mother must keep warm. No doubt all this was well meant, but it irritated Wolfgang who was now an adult with his own thoughts and opinions. Why couldn’t his father be less controlling? Also, Leopold wanted him to be as meticulous as himself: <strong>“Why can’t you write letters the way I do?”</strong> Leopold would chide him. <strong>“If I break off a letter and begin again the next day, I write down whether it’s Sunday or Monday.”</strong></span></p>

<p>Not that Wolfgang’s letters were not detailed but they were written in a style completely unlike Leopold’s and were not suitable as publishing material. Following is an excerpt taken from one of Wolfgang’s letters to his father from Mannheim regarding a party with the Mannheim Kappelmeister Christian Cannabich:</p>

<p><strong>“I, Johannes Chrisostomus Amadeus Wolfgangus Sigsimundus* Mozart, am guilty of not coming home until 12 o’clock midnight… from 10 o’clock until said hour at Canabich’s, I did some rhyming in the presence of said Canabich, his wife and daughter… nothing too serious, but rather light and frothy, actually nothing but crude stuff… I would not have behaved so godlessly if our ringleader, known as Lisel, namely Elisabetta Canabich, had not inspired and excited me to such high degree. I must also confess that I thoroughly enjoyed it all.”</strong></p>
