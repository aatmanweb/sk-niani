<div id="biography-script-boxCenter">
<p>After a brief rest, the whirlwind trip began again in June 1763 and lasted for three and a half years. Stops were made in Munich, Bonn, Cologne, Brussels, Paris, Versailles, London, The Hague, Geneva and Zurich.</p>
<p>The Mozart’s spent the Christmas of 1763 at the French court of Queen Maria Leszczynska and Louis XV (a Bourbon monarch) who ruled as King of France and Navarre. On New Year’s day, the Mozart family was invited to court dinner with the royal couple. Leopold narrated, <strong>“My Wolfgang was graciously privileged to stand beside the Queen the whole time, to talk constantly to her, entertain her and kiss her hands repeatedly, besides partaking of the dishes which she handed him from the table… I stood beside him, and on the other side of the King… stood my wife and daughter.”</strong></p>

<p>The proud father invited many observers to test the talents of the wünderchild. Daines Barrington, a prominent English lawyer, antiquary and naturalist conducted one such test when Wolfgang was eight. The young lad was given a manuscript composed with five part open score of a new opera by a London composer. To add to the problem, two of the five lines were in the offbeat Italian style <em>Contralto Clef</em> which Wolfgang had never seen before. Did the young Mozart pass the test? He certainly did; not only did he play the piece flawlessly, but he also captured the composer’s tempos, dynamics and musical intentions. The British lawyer was stupefied by Wolfgang’s talent and described him as an <strong>“eight year old who stood seven feet tall.”</strong></p>
</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Clef</strong>- A clef or key is a musical symbol used to indicate the pitch of written notes.<strong>Contralto</strong>-The part sung by the highest male or lowest female voices.</p>

</div>

<div id="biography-script-boxCenter">
<p>While he was enthralling the royalty with his beautiful performances, the child musician was also composing endlessly. Between 1763 and1765, Wolfgang composed two Sonatas in Paris, seven Sonatas, two Symphonies, two Arias, a Motet and many untitled pieces in London and an Aria as well as a Symphony while in The Hague. Young Wolfgang was certainly a gifted child! Also, during this time, he met a great number of musicians and got acquainted with the work of other composers. In 1764-65, he visited Johann Christian Bach (a composer of the Classical era) in London, who particularly had an important influence on him.</p>
</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Sonata</strong> – A composition for one or more solo instruments, one of which is usually a keyboard.<strong>Symphony</strong> – A long piece of music for an orchestra composing of many different instruments.</p>
<p><strong>Aria</strong> – An extensive and complex song for solo voice.</p>
<p><strong>Motet</strong>- A polyphonic choral composition with sacred lyrics; sung as a part of church service.</p>

</div>

<div id="biography-script-boxCenter">
<p>But life was not all thrill and magic for the young musician and his family. Travelling in those days had to be done by horse and coach, which was painfully slow and left one to the mercies of the weather. It was bitterly cold in winter, scorching hot in summer and nothing to say of the rains. Also, the family finances were never secure. If the Mozart’s were to be invited as house guests to wealthy houses, they had to make contact with the rich and pander to their whims and fancies, one of which was their habit of gifting silver snuff boxes instead of cash. Leopold felt this deeply and wrote: <strong>“We have swords, laces, mantillas, snuff boxes, gold cases, sufficient to furnish a shop; but as for money, it is a scarce article, and I am positively poor.”</strong> Also, this wandering circus life did not agree with the children and they frequently fell ill – strep throat, rheumatoid arthritis and typhoid fever, Wolfgang had them all.</p>

</div>








<h1>Salzburg again</h1>
<p><img src="/uploads/script/mozart/mozart-salzburg-again.jpg" alt="mozart-salzburg-again"></p><br />
<div id="biography-script-boxCenter">

<p>Wolfgang was eleven when the family returned to Salzburg. The young boy now appeared as a composer and child prodigy. Here, he composed the Latin opera <em>‘Appollo et Hyacinthus’,</em> widely believed to be his first real opera. He also composed a set of funeral music known as Grabmusik. But Leopold’s wanderlust soon made the family set off on yet another trip; within nine months the family was on its way to Vienna where they hoped to present Wolfgang’s first Italian opera buffa <em>‘La finta semplice’</em> (The Pretended Simpleton). But a smallpox epidemic forced them to flee to Moravia (now in Czech Republic) and then to Olmutz (or Olomouc in Czech Republic) where the children succumbed to the disease. Once the children recuperated, the family returned to Vienna, but the opera could not be presented as rumours had started doing the rounds that a mere child could not have composed music for an entire opera. Furious, Leopold herded the family back to Salzburg where the opera was performed only once in 1769. By this time, the 13 year old genius had already composed the first 65 pieces in the Kochel catalogue.</p>
</div>

