<p>Leopold who was likewise ravaged, urged his son to come back to Salzburg where he had wangled a job for him as a court organist. Wolfgang abhorred the idea of going back to Salzburg and delayed his return as long as he could. Perhaps needing some emotional support, he decided to go back to Mannheim to meet Aloysia. Wolfgang still clung to the hope of finding employment outside Salzburg. Leopold was incensed with his wayward son and wrote him a stinging letter: <strong>“I really don’t know what to say anymore. I will either lose my mind or die of exhaustion… Your whole intent is to ruin me so you can build your castles in the air… I hope that, after your mother had to die in Paris already, you will not also burden your conscience by expediting the death of your father.”</strong> Obdurate, Wolfgang still went to Mannheim but he had to leave empty handed as there was no job for him and no Aloysia either. She had moved to Munich. Wolfgang then went to Munich where he encountered a completely different Aloysia; she was successful and seemed to have no feelings or time for him. Defeated, he decided to return to his prison, Salzburg. He wrote to Leopold: <strong>“I’m looking forward to seeing you but not Salzburg… I cannot stand Salzburg and its inhabitants… I find their language – their manners quite insufferable.”</strong></p>

</div>














<h1>Last spell in Salzburg</h1>
<p><img src="/uploads/script/mozart/mozart-last-spell-in-salzburg.jpg" alt="mozart-last-spell-in-salzburg"></p>

<div id="biography-script-boxCenter">

<p>In February 1779, Wolfgang began his duties as a court organist in Salzburg where he was responsible for composing new works. No commissions seemed forthcoming so Wolfgang immersed himself with heart and soul into composing some of his most beautiful sacred and orchestral music to date. Despite this, once he left Salzburg for Vienna he insisted that he had found it very difficult to get down to serious work in Salzburg.</p>

<p>The seeds sown in Mannheim and Paris finally took root and in 1780, Wolfgang he was offered a commission for an opera the <em>Idomeneo</em>, <em>re di Creta</em> or <em>Idomeneo</em>, King of Crete in Munich. This is considered by many to be his greatest opera. The jubilant Wolfgang was soon in Munich, composing furiously. This was his first opera in five years and he put all his energy into it. One of the characters of the opera was the sea god, Neptune. The perfectionist that Wolfgang was, he fixed this speech four times, reducing the words in each setting, for he believed that the jolt of hearing a supernatural voice was quickly lost. As Wolfgang wrote to Leopold – <strong>“If the speech of the ghost in ‘Hamlet’ were not so long, it would be much more effective.”</strong></p>

<p>Father and son again came close, if only professionally, as they exchanged letters over the opera. But some of the ache still lingered, though both tried hard to go back to the old easy footing. When Wolfgang was down with a cold, Leopold advised him on treatment and proposed to come and stay with him should his cold worsen. However, he could not stop himself from adding a spiteful little remark: <strong>“had I been with your mother, I would like to believe, she would still be alive”</strong>. Leopold came for the performance of the opera though, and also brought along Nannerl. The audience liked the opera but it was performed only three times in Munich.</p>

<p>Wolfgang was still in Munich savouring the success of his opera and wondering if he could dare to hope for more commissions, when he was summoned by Archbishop Colloredo to Vienna, where the latter had gone to visit his own father. Accordingly, Wolfgang set off for Vienna, while Leopold went back to Salzburg. Little did the two realize how significant this parting would turn out to be.</p>

</div>


















<h1>Vienna begins with a Mighty Bang</h1>
<p><img src="/uploads/script/mozart/mozart-vienna-begins-with-a-mighy-bang.jpg" alt="mozart-vienna-begins-with-a-mighy-bang"></p><br />
<div id="biography-script-boxCenter">

<p>The young composer set off for Vienna with high hopes. He felt sure that he could make a career for himself in <em>the land of the Clavier</em>. But how could he forge useful contacts when he was supposed to hang around in the corners waiting for the Archbishop’s summons? Also, who knew when he would be packed off to Salzburg? He was not about to let go of such a golden opportunity, not Wolfgang Amadeus Mozart. Accordingly, he went ahead and started making contacts with potential patrons, musicians and theatre folk. When the Archbishop came to know of his <em>servant’s </em>activities, he was enraged and he accused Wolfgang of being a scoundrel, a lousy rogue, a cretin. However, it must be noted that the source for this entire fracas is Wolfgang’s letters to his father and so the information is essentially one sided.</p>

<p>The seemingly naive Wolfgang felt sure that this was the sack and he wrote to his father that he had answered back. However, he was told that a spoken word has no legal standing so for all practical purposes, he was still an employee of Archbishop Colloredo. Also, how could he resign – he was a servant, wasn’t he? The only option open to him was to apply in writing to be dismissed from his position and this would have to be backed by his father’s permission though he was now 25 years old!</p>

<p>Finally and as per Wolfgang, literally, he got the order of boot from Count Arco, the Archbishop’s personnel manager. Earlier, Count Arco like Leopold had tried to persuade Wolfgang to be more reasonable but this only succeeded in inciting the young man further and he wrote<strong>: “by refusing to accept a petition… but not informing your master because you lack the courage to do so and because you love being a pedantic bureaucrat, you keep a fellow dangling for four weeks and, finally as this fellow is forced to hand in his petition himself, you throw him out the door and give him a kick on his Behind – And that’s the Court who, according to your last letter, has only my interest at heart…”</strong></p>

<p>Wolfgang, carried away by his youth, badly wanted to execute a return kick but this was foolhardy as attacking a person of higher rank could lead to imprisonment or a whipping. As expected, Leopold wanted him to come back to Salzburg but Wolfgang was in two minds about it. His father had some cause for worry as nasty rumours had reached Salzburg about the company which his son was keeping and his supposed drinking habits. Wolfgang went to great pains to deny all this and wrote: <strong>“You need not worry about the salvation of my soul.”</strong> Perhaps there was some truth to these rumours but his productivity proved that whatever else Wolfgang was doing, he was working very hard. In the end, much to his father’s chagrin, Wolfgang decided to stay on in Vienna as a freelance performer and composer.</p>

</div>














<h1>Wolfgang and Constanze</h1>
<div id="biography-script-boxCenter">

<p><img src="/uploads/script/mozart/mozart-wolfgang-and-constanze.jpg" alt="mozart-wolfgang-and-constanze"></p>

<p>Even before his summary dismissal by the Archbishop, Wolfgang had moved to private lodgings. This irked Leopold still further as the household was that of the Weber family. Fridolin had died and his widow and three unmarried daughters Josepha, Constanze and Sophie had settled down in Vienna. His old beloved Aloysia had married Joseph Lange, an actor and painter and was now a leading court singer. Gossip soon linked Wolfgang’s name with Constanze’s and he had to leave their house and move to other lodgings. He tried to make light of the whole Constanze angle to his father and said that gossips often linked him with his accomplished pupil Josepha Auernhammer, but there was no cause for worry: <strong>“She is heavy like a peasant wench, sweats to make you sick… People are wondering why I would take someone with a face like hers.”</strong></p>

<p>However, his father was not fooled and in December 1781, Wolfgang came clean and admitted that he was committed to marry Constanze Weber. The Weber family wanted the two to get married quickly and when Wolfgang came to know that Constanze’s mother and guardian planned to compromise the couple so as to force them to marry, he refused to be a party to it. He did not find her beautiful though and maintained: <strong>“She is not ugly, but her whole beauty consists of two little black eyes and a graceful figure. She has no great wit but enough common sense to fulfil her duties as a wife and mother”</strong>. But he subtly praised her in his letters to Leopold trying to convince him to the match but Leopold was in no mood to listen.</p>

<p>In the midst of all this personal drama, the talented composer’s professional life was starting to look up. The second half of 1781 found him busy composing for the opera <em>‘Die Entfuhrung aus dem Serail’ </em>or <em>‘The Abduction from the Harem’</em>. The opera was actually scheduled for September 1781 but was finally performed in July next year. Wolfgang used this additional time to great effect; the genius that he was, he wrote music for the anxious beating of the hero’s heart as he waits to see his beloved. The opera was performed and it turned out to be a success and received a good number of performances. Once again Wolfgang hoped for follow-up commissions and yet again he was disappointed. The young composer had to find other ways to earn a living as he was getting married soon. He devoted himself to composing and created some very fine pieces.</p>

<p>Finally at the age of 26, Wolfgang married Constanze in Vienna’s St. Stephen’s Cathedral on 4th August, 1782. Leopold and Nannerl were against the marriage as they thought the Weber family to be socially inferior to them and hence never managed to develop a good rapport with Constanze. However, Leopold did consent to the wedding at the end and his letter agreeing to the marriage arrived just a day after the wedding. The couple had six children together, of which only two survived infancy.</p>

<p>Wolfgang was extremely happy and his way of showing happiness was by composing a huge ceremonial work. However, he never completed it as he realized that it held no appeal for the Viennese and had no saleable value either. Some of this music was performed during Wolfgang and Constanze’s only visit to Salzburg as a married couple in 1783. This was the last time that Nannerl saw her brother as she soon got married. The same year their first child, a son was born. They named him Reimund Leopold Mozart; sadly, he survived only for two weeks. Soon their second child, Karl Thomas, was born on 21st September, 1784 and was duly admired by Leopold when he came to visit them in the following year. He wrote to Nannerl that he was impressed by Wolfgang’s contacts, house and prestige.</p>

<p>In the same year, Wolfgang became part of the Freemasons. He faithfully attended the meetings and his work came to include a small category called Masonic music. The young composer’s thoughts on life and death were vastly affected by the teachings of the masons. The brotherhood was strong and many a times Wolfgang profited from the help of his brothers when in financial difficulties.</p>
</div>


<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p>Freemasons are ordinary men in the community, of various religious backgrounds, who share a concern for human values, high moral standards, respect for the laws of society and the rights of others. Their practice of Freemasonry promotes self development, family, charitable and community service and socialisation with likeminded individuals and their families.</p>

</div>





















<h1>The roller coaster years begin</h1>
<p><img src="/uploads/script/mozart/mozart-the-roller-coaster-years-begin.jpg" alt="mozart-the-roller-coaster-years-begin"></p>
<div style="width: 445px; float: left; height: auto; margin-left: 35px;">

<p>Wolfgang’s early years in Vienna were happy and busy ones. He was composing all kinds of instrumental music and was also studying the works of other composers, which at times influenced his own style. Joseph Haydn, one of the most famous composers of that period, reportedly told Leopold on one of his visits to Vienna, <strong>“As God is my witness and I an honest man, I say that your son is the greatest composer I know, in person or by reputation. He has taste and moreover the profoundest skill in composition.”</strong></p>

<p>From 1782 to 1785, Wolfgang gave many self-produced concerts in which he was the ‘<em>soloist’</em>. Theatre space in Vienna was scarce and so he used to book unorthodox venues such as big rooms in residential buildings and ballrooms of expensive restaurants. The concerts were popular as Wolfgang enjoyed a special bond with his audience. The year 1784 was his most prolific year; during one five week period, he appeared in 22 concerts. At this time, he also began to keep a catalogue of his own music.</p>
</div>
















<h1>An opera again</h1>
<p><img src="/uploads/script/mozart/mozart-an-opera-again.jpg" alt="mozart-an-opera-again"></p>

<div id="biography-script-boxCenter">
<p><a href="/uploads/script/mozart/019.jpg" rel="attachment wp-att-1641"><img class="alignnone size-full wp-image-1641" alt="019" src="/uploads/script/mozart/019.jpg" width="600" height="378" /></a></p>

<p>In 1786, Wolfgang made a re-entry in opera with <em>Le nozze di Figaro </em>or<em> The Marriage of Figaro.</em> This was his first opera with the librettist Lorenzo Da Ponte. The two wrote a total of three operas together – theirs was a partnership which gave the world perhaps the best operas in the range. Figaro was popular but it got only nine performances. The same year tragedy struck again when their third child, Johann Thomas Leopold Mozart was born, but he died after four weeks.</p>

<p>Early in 1787, Wolfgang went to Prague with this opera where it was a smashing success. Wolfgang was in the seventh heaven of delight and he wrote to a friend: <strong>“everyone was hopping about in sheer delight to the music of my ‘Figaro’, which had been transformed into Contredanses and German dances; for here they talk of nothing but – ‘Figaro’; nothing is played, blown, sung or whistled but – ‘Figaro’: no opera is seen as much as – ‘Figaro’; again and again it is ‘Figaro’; – it’s all a great honor for me.”</strong> For once, Wolfgang obtained another commission to write an opera, the ‘<em>Don Giovanni’</em>.</p>

</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Librettist</strong>- Author of words for an opera.</p>

</div>














<h1>Double toil and trouble</h1>
<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-double-double-taoil-and-trouble.jpg" alt="mozart-double-double-taoil-and-trouble"></p>

<div id="biography-script-boxCenter">

<p>By 1787, Wolfgang’s situation changed for the worse and the Mozart’s couldn’t keep up with the demands of their extravagant lifestyle. The large apartment was starting to pinch and in April, the family moved to a less expensive house. Wolfgang’s most lucrative business, teaching, had its own set of problems. Wolfgang had to travel to his pupils’ homes for giving lessons which severely strained his pocket. If he had to appear respectable he had to look equal in worth to those who paid him. That meant taking utmost care of his personal appearance which was not entirely unwelcome as he delighted in such things.</p>

<p>Good clothes were Wolfgang’s weakness. An excerpt from one of his letters shows his fondness for dress: <strong>“I simply must have such a jacket so it will be worth my effort to get those buttons, which I can’t get out of my mind… mother-of-pearl with several white stones around the edge and a beautiful yellow stone in the middle.”</strong> He was very proud of his fine hair and had them attended to every day; a barber would come each morning to curl, dress and powder it.</p>

<p><a href="/uploads/script/mozart/double-double-taoil-and-trouble01.jpg"><img class="size-full wp-image-2467 aligncenter" alt="double-double-taoil-and-trouble01" src="/uploads/script/mozart/double-double-taoil-and-trouble01.jpg" width="613" height="465" /></a></p>

<p>During this month, a 16-year old lad named Ludwig van Beethoven came to him for some lessons but had to leave after just two weeks as his mother fell ill. The two never met again. Soon, Wolfgang was sick with kidney problems. His childhood friend, Dr. Sigmund Barisani also died in this year and so did Wolfgang’s pet bird. Wolfgang and Constanze’s fourth child, Theresia Constanzia Adelheid Freiderika Maria Anna was born towards the end of the year but she too, died six months later. However, the biggest tragedy came in the form of Leopold’s death on 28th May, 1787. Leopold had been ill for some time but his death was sudden. Wolfgang did not get the chance to go to Salzburg to see his father one last time. Wolfgang’s last letter to his father was written on April 14.</p>

</div>
















<h1>The Letter:</h1>
<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-the-letter.jpg" alt="mozart-the-letter"></p>

<div id="biography-script-boxCenter">

<p>No doubt, Leopold was glad to hear from his son but he soon died and Wolfgang never saw his father again. He felt the death of his father severely; they had been through so much together that it must have been very difficult for him to adjust to a life where there was no Leopold to write to, to listen to, even to get exasperated with! What Wolfgang needed now was a good steady job and that meant a court job. But this continued to remain elusive with the court’s preference for Italians and the influence of the Kappellmeister Antonio Salieri. Letters between father and son show, that both of them distrusted Italian musicians in general and Salieri in particular.</p>

</div>













<h1>Success and failure</h1>
<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-success-and-failure.jpg" alt="mozart-success-and-failure"></p>

<div id="biography-script-boxCenter">

<p>There was still Don Giovanni to look forward to and it turned out to be a success. But like always, there were no new commissions approaching. In December 1787, Emperor Joseph II appointed Wolfgang as his <em>‘chamber composer’</em> as the post had fallen vacant with the death of Gluck, the legendary opera composer. Though the gesture was meant to distinguish Wolfgang, the emperor had an ulterior motive as well. Joseph didn’t want him to leave Vienna. The pay was less and his responsibility was only to compose dances for the annual balls. Wolfgang supposedly said that the salary was <strong>“too much for what I do; too little for what I could do.”</strong> However even this modest income became important to him.</p>

<p>Situations worsened in 1788, when the emperor declared war on the Turkish Empire. The finances of the country suffered and the populace’s love for music took a back seat as people had increasingly less time and more importantly, less money for spending on music. People were still ready to shell out money for operas though, but unfortunately, Wolfgang didn’t have any operas on hand. Ironically, other musical activities (teaching and concert giving), which were his bread and butter, suffered grievously. The troubled composer’s financial situation grew dire and he was forced to pawn his valuables again. The next year, his fifth child daughter, Anna Maria Wolfgang was born in late November. She survived merely for an hour.</p>

<p><a href="/uploads/script/mozart/success-and-failure01.jpg" rel="attachment wp-att-1643"><img class="alignnone size-full wp-image-1643" alt="success-and-failure01" src="/uploads/script/mozart/success-and-failure01.jpg" width="600" height="459" /></a></p>

<p>Wolfgang now threw himself into his work harder than ever but he could not compose much even though he regularly burned the midnight oil. He began to slip into depression and had frequent mood swings. Hoping to improve his fortunes, he made long journeys to Leipzig, Dresden and Berlin in the spring of 1789 and to Frankfurt, Mannheim and other German cities in 1790. However, these trips procured only isolated success and did not do much to relieve the family’s financial difficulties.</p>

<p>In 1790, Wolfgang finally got an opera commission ‘<em>Cosi fan tutte’ or All women do this</em>, ‘<em>this</em>’ meaning ‘<em>latch onto the nearest man.’</em> The opera which was again by Da Ponte was originally intended for Salieri who declined it. In hindsight, it seems he was wise to refuse it; the opera caused a lot of public outrage for its supposed ‘immoral’ plot. Reputed composers were irked with Wolfgang for having had anything to do with such an opera. Nevertheless, the opera was successful but its run of performances was interrupted by the death of Emperor Joseph II in February 1790. The new Emperor, Leopold II, Joseph’s brother, was a great lover of music but unfortunately for Wolfgang, he didn’t like the composer. The new emperor completely ignored Wolfgang and did not invite him to his coronation in Frankfurt. Wolfgang went anyway, hoping some work might come his way. However, the trip was not a financial success and the dispirited Wolfgang returned back to Vienna.</p>

</div>

















<h1>The irony of fate</h1>
<img src="/uploads/script/mozart/mozart-the-irony-of-fate.jpg" alt="mozart-the-irony-of-fate">

<div id="biography-script-Right">

<p style="padding-left: 90px;">In 1791, things finally began to look up for Wolfgang. In January, he completed his last piano concerto (K.595) and played it at a benefit concert. Unbeknown to the young maestro, this was to be his last public performance. He produced two beautiful Fantasias (K.594, K.608) as part of his court duties; two Masonic Cantatas (K.619, K.623) and one Clarinet Concerto (K.622). However, the principal works of this year were his two operas; for the first time in his life and unfortunately the last, he had two operas in a year!</p>

<div style="width: 460px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 45px;">
<p><strong>Fantasias-</strong> A free form musical composition.</p>
<p><strong>Concerto-</strong> A composition for a soloist and an orchestra.</p>

</div>

<p style="padding-left: 90px;">‘<em>Die Zauberflote</em>’ or The Magic Flute premiered in September; the ‘Singspiel’ contained a delightful blend of adventure, magic and music and managed to connect with the audience’s hearts. Before Zauberflote’s premiere however, Wolfgang received an unforeseen commission. It was an opera seria commissioned for Leopold II’s coronation as King of Bohemia. This opera called ‘<em>La clemenza di Tito’</em> or <em>The Mercifulness of Titus</em> was originally offered to Salieri, who declined it and the work came to Wolfgang. The young composer was delighted as it was to be performed in Prague, where he was still a favourite. Wolfgang had to work feverishly to complete it owing to shortage of time. When the opera was ultimately performed, the Empress found it boring and told a friend that the music was very bad. However, Prague liked it and <em>La clemenza</em> was widely performed in the 1790s and became Wolfgang’s first opera to be performed in London (in 1806).</p>

<div style="width: 460px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 45px;">
<p><strong>Singspiel-</strong> A German comic opera with spoken dialogue.</p>
</div>

</div>












<h1>The curtain falls</h1>

<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-the-curtain-falls.jpg" alt="mozart-the-curtain-falls"></p>

<div style="width: 350px; float: left; height: auto; background: #EFEFEF; margin-left: -30px; padding-left: 100px; padding-right: 30px; margin-top: -50px;">

<p>Wolfgang’s last composition was a requiem commissioned by an eccentric count called Franz Walsegg-Stupach in memory of his deceased wife. Perhaps to avoid publicity, the count himself remained anonymous. From this arose many dark rumours: the story that ‘a grey stranger’ visited Wolfgang warning him of his approaching death and that the composer intended the Requiem for himself; the tale that Constanze took away this work as it made Wolfgang ill and the story of the composer directing its rehearsal and singing the alto part hours before his death.</p>

<div style="width: 300px; height: auto; padding: 20px 20px 5px 80px; background: #fff url('/uploads/script/mozart/note-icon2.jpg') left top no-repeat; margin-left: -50px;">
<p><strong>Alto</strong>- The highest male adult singing voice.</p>

</div>

<p>July 1791, Wolfgang’s 6th and last surviving child, Franz Xaver Wolfgang Wolfgang was born. However, this time it was Wolfgang who would not survive to see his child grow.</p>

<p>Wolfgang was excited about the Requiem as it would allow him to use his wide experience in sacred music. He began work on it and had completed the first two sections of the proposed four when he had to abruptly break off from work as he fell ill. There are reports which say that he had fallen ill while in Prague but the veracity of such claims is questionable. As far as we know, Wolfgang fell ill in late November, only two weeks or thereabouts before his death. There was considerable swelling in his body which made it impossible for him to move. Also he had contracted rheumatic fever, suffered from frequent bouts of vomiting and perhaps his kidneys had failed. However what rang the death knell for the moribund composer was the application of cold compresses to his forehead. This well-meaning ministration of a certain Dr. Closset only succeeded in making Wolfgang lose consciousness. The cold compresses came as a shock to his overheated body.</p>

<p>Wolfgang died in the early hours of December 5, 1791 at the young age of 35. He was buried two days later in an unmarked grave just outside Vienna. The simple funeral was Viennese tradition and the story that he was buried in haste in a pauper’s grave is mere fiction. However, his memorial services and concerts in Prague and Vienna were well attended.</p>
</div>













<h1>A natural death or murder</h1>
<p style="padding-left: 60px;"><img src="/uploads/script/mozart/mozart-a-natural-death-or-murder.jpg" alt="mozart-a-natural-death-or-murder"></p>
<div id="biography-script-boxRight">

<p>Did Wolfgang die a natural death or was he murdered? There has been much speculation on the reason of Wolfgang’s death. His death was most likely the result of a severe fever which had led to the death of several other Viennese as well. Wolfgang, who had never been especially robust, had simply succumbed to it. However, there are many, whose morbid nature rejects the idea of a natural death. Some claim that Wolfgang was poisoned by Salieri. This claim seems preposterous as the latter was much more successful when both were alive and would have had no motive for such an act. Wolfgang’s last letter itself reports that Salieri congratulated him warmly on ‘Die Zauberflote’ and called it an operone (a grand opera). They were professional rivals, no more no less.</p>

<p>Another story replaces Salieri by the jealous husband of Maria Magdalena Hofdemel, one of Wolfgang’s pupils. Her husband attacked his pregnant wife a day after Wolfgang’s death and then himself committed suicide. However, there is no proof that he killed her for any infidelity of hers or even if that was the case, Wolfgang was involved. There is no dearth to the stories of Wolfgang’s supposed murder. If not a rival or a jealous husband, then there is always a freemason brother or the government who could have done the vile deed. A commonplace death through fever seems just too tame a death for one of the most legendary composers that the world has ever witnessed. But as we assume and believe, Wolfgang died a natural death.</p>

</div>













<h1>Life after death</h1>
<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-life-after-death.jpg" alt="mozart-life-after-death"></p>
<div id="biography-script-boxCenter">

<p>Ironically, the world woke up to Wolfgang Amadeus Mozart very soon after his death. Publishers, singers and audiences started discovering Mozart’s uniqueness and were mesmerized by the beauty of his compositions. His wife Constanze and her second husband, the Danish diplomat Georg Nikolaus Nissen must be credited for slaving over publishing his unpublished music, completing incomplete works and preserving the surviving documentation of Mozart’s life.</p>

<p>Mere life is not a victory, mere death is not a defeat… this holds perfectly true for Mozart. The maestro still lives on in the world through his timeless compositions. His succulent melodies irresistibly pull at one’s heart. Centuries have passed since Mozart composed his melodies, but they have stayed ever fresh and always manage to make a connection with whosoever listens to it, be it young or old, an amateur or a professional. There is something in his music for each one of us. The world is a better place today because of Mozart’s music.</p>

<p>So powerful is his music that it seems to have a healing power. When his sonata K.448 was played to epilepsy patients, it reportedly led to decreased epileptic form of activity. Studies claim that listening to Mozart every day improves mathematical skills, reduces stress and improves IQ. This theory has been termed as “Mozart effect” by the French researcher Dr. Alfred A. Tomatis, in his book “Pourqoi Mozart” or Why Mozart? His music seems to find more uses every day. A German sewage treatment plant plays Mozart’s music to break down the sewage waste faster. The plant operators supposedly believe that the vibrations of Mozart’s music break through everything, including water, sewage and cells. It’s unclear whether Mozart’s music is really that powerful. There is only one way to find out, listen to some Mozart today, who knows you might just get a 100 on your next test.</p>

