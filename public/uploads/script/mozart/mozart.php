<h1>Birth of the Wunderchild</h1>
<div id="biography-script-boxCenter">

<p><img src="/uploads/script/mozart/mozart-birth-of-the-wunderchild.jpg" alt="mozart-birth-of-the-wunderchild"></p>

<p>It was the winter of 1756. A family in the town of Salzburg, Austria was celebrating the arrival of their seventh child into the world. The baby boy was christened Joaness Chrysostomus Wolfgangus Theophilus Mozart. The first part of his name was in honor of St. John Chrysostom, as the baby’s birthday, 27 January, was the ‘feast day’ of Saint John. Wolfgang was the name of his maternal grandfather. The name Theophilus (Greek word for ‘loved by God’) was in honor of Mozart’s godfather Joannes Theophilus Pergmayr. As a child, he was called by the name Wolfgang or the nickname Wolfgangerl. In later years, he often added Theophilus but translated it into Amade (French), Gottlieb (German) or Amadeus (Latin). Today the world knows him best as Wolfgang Amadeus Mozart.</p>

<p>Wolfgang was the sole surviving son of Leopold Mozart and his wife Anna Maria. Only two of the couple’s children survived past infancy – Wolfgang and his sister Maria Anna Walburga Ignatia (nicknamed Nannerl) who was five years elder to him. There existed a close bond between the brother and sister. Nannerl was Wolfgang’s confidante and his <em>carissima sorrella</em> or dearest sister.</p>

<p>Wolfgang’s father, Leopold, was a talented violinist and composer who regularly performed in churches and noble courts. He was also a music teacher and his book on the fundamental principles of violin playing ‘<em>Versuch einer grundlichen Violinschule’</em> (A Treatise on the Fundamental Principles of Violin Playing), was one of the best books of its time for violin training and is still used today. Leopold worked as a violinist in the court orchestra of The Prince Archbishop of Salzburg, a position which he held for 44 years under five successive Prince Archbishops. He started as a fourth violinist and gradually worked up to the position of Deputy Kappellmeister in 1763 under Count Sigismund Christoph Schrattenbach. Wolfgang’s mother, Anna Maria, was a gentle and kind woman with a strong sense of duty.</p>
</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Archbishop</strong> – A bishop is an ordained or consecrated member of the Christian clergy who is generally entrusted with a position of authority and oversight. An Archbishop is a bishop of higher rank.</p>
</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Kappellmeister</strong> – a German word referring to a person in charge of music making. The word is a compound of Kappelle which means choir, orchestra or chapel and Meister which denotes master.</p>
</div>

<div id="biography-script-boxCenter">
<p>The Mozart’s were a very close knit family. They were neither very wealthy nor too poor. Their apartment where Wolfgang was born was conveniently central and had enough space to practice music. The Mozart home was frequently visited by friends and acquaintances and was one of the centres of domestic musical life in Salzburg.</p>
</div>










<h1>Enter the magical world of music</h1>

<div style="width: 360px; height: auto; float: left; background: #000; position: relative; text-align: right; padding: 40px 30px  100px 30px;">

<p style="text-align: left;"><img src="/uploads/script/mozart/mozart-enter-the-magical-world-of-music.jpg" alt="mozart-enter-the-magical-world-of-music"></p>

<p><a href="/uploads/script/mozart/enter-the-magical-world-of-music01.jpg" rel="attachment wp-att-1632"><img class="size-full wp-image-1632" alt="enter-the-magical-world-of-music01" src="/uploads/script/mozart/enter-the-magical-world-of-music01.jpg" width="350" height="246" /></a></p>

<p><a href="/uploads/script/mozart/enter-the-magical-world-of-music02.jpg" rel="attachment wp-att-1633"><img class="size-full wp-image-1633" alt="enter-the-magical-world-of-music02" src="/uploads/script/mozart/enter-the-magical-world-of-music02.jpg" width="350" height="246" /></a></p>

<p><a href="/uploads/script/mozart/enter-the-magical-world-of-music03.jpg" rel="attachment wp-att-1634"><img class="size-full wp-image-1634" alt="enter-the-magical-world-of-music03" src="/uploads/script/mozart/enter-the-magical-world-of-music03.png" width="350" height="246" /></a></p>

<p><a href="/uploads/script/mozart/enter-the-magical-world-of-music04.jpg" rel="attachment wp-att-1631"><img class="size-full wp-image-1631" alt="enter-the-magical-world-of-music04" src="/uploads/script/mozart/enter-the-magical-world-of-music04.jpg" width="350" height="246" /></a></p>
</div>
<div id="biography-script-boxRight">
<div style="padding: 430px 70px 0px 0px;">

<p>Wolfgang’s was not an ordinary childhood. The boy never went to school and was instead coached by his father at home. The young boy was very good at Mathematics and he would scribble mathematical figures all over the walls and tablecloths; he could also speak in seven different languages! The musician Leopold introduced his children to music at a very early age. Nannerl’s musical education began at seven on the Clavier (keyboard). Little Wolfgang would look on eagerly while his sister played. The keyboard soon became the baby’s most loved toy and he would spend hours picking out tunes on it. Noting his son’s interest, Leopold began teaching him music since the age of four.</p>

<p>Leoplod began by teaching the young Wolfgang <em>Minuets</em> (a type of slow music). Not only did the child pick it up very quickly but he also managed to play it effortlessly and with great sensitivity. The young musician could not have enough music and very soon he even learnt to play the violin. By the time he was five, Wolfgang was composing short pieces which he would play to his father who would write them down. Leopold taught his children well; he made the lessons fun and interesting but at the same time demanded utmost concentration and perfection. As their music teacher, he knew that his children were exceptionally talented and could easily become a bankable asset for him. To this end, he risked his children’s health and perhaps their childhood as well. But one should not judge him too harshly; he was only human and after all, he sacrificed his own career for the sake of his children. Also, Leopold still dreamt of making a name for himself, if not as a composer than as a writer. In those days, writers were generally more respected than musicians. Musicians were at par with the cooks, butlers and maids; just another type of servant hired to serve the rich. So, right from the early days, Leopold maintained a detailed record of everything concerning Wolfgang, mostly through diary-like letters which he sent to his friends when away from Salzburg. Perhaps he aimed to have them made into a biography later.</p>
</div>
</div>








<h1>The travelling circus</h1>
<p><img src="/uploads/script/mozart/mozart-the-travelling-circus.jpg" alt="mozart-the-travelling-circus"></p><br />
<div id="biography-script-boxRight">

<p>When Wolfgang was six, his father’s employer Count Schrattenbach, granted Leopold leave to take his children on a grand tour of European courts. Their first court performance was for Maximilian III Joseph, the Elector of Bavaria, in Munich, January 1762. Between September 1762 and January 1763, the brother-sister duo performed in Austria. At the young age of six, the little Wolfgang had the honor of performing before Emperor Francis I and Empress Maria Theresa. When the little boy slipped on the polished floor of the court, a small girl helped him back on his feet. This girl was Marie Antoinette who would later become the Queen of France!</p>

</div>
<p><a href="/uploads/script/mozart/the-travelling-circus01.jpg" rel="attachment wp-att-1635"><img class="alignnone size-full wp-image-1635" alt="the-travelling-circus01" src="/uploads/script/mozart/the-travelling-circus01.jpg" width="900" height="636" /></a></p>

<div id="biography-script-boxLeft">
<p>How many parents can boast of children who have performed before royalty at such a tender age? The children’s parents were justifiably proud and Leopold wrote of this momentous occasion, <strong>“Their Majesties received us with such extraordinary graciousness that, when I shall tell of it, people will declare that I have made it up. Suffice it to say that Wolferl jumped up on the Empress’ lap, put his arms around her neck and kissed her heartily.”</strong></p>
</div>

