<div id="biography-script-boxCenter">
<p>Observing his son’s spectacular progress, the stage parent Leopold decided to take his son to Italy, the land of music. In December 1769, the duo set off, leaving Nannerl and her mother behind in Salzburg. Nannerl’s professional music career seemed to have come to an end. The girl was reaching marriageable age and during those days it was considered improper for young girls to show off their artistic talent in public.</p>
</div>
















<h1>The first opera</h1>
<p style="text-align: center;"><img src="/uploads/script/mozart/mozart-the-first-opera.jpg" alt="mozart-the-first-opera"></p>
<div style="width: 430px; margin: 0 auto; height: auto;">

<p>Father and son reached Rome in time for Easter and it was here that Wolfgang performed one of his most remarkable feats. The Sistine chapel in the Papal enclave of the Vatican was considered the centre of vocal music. They covetously guarded the renowned Italian composer Gregorio Allegri’s famous setting for the ‘<em>Miserere</em>’ which was a setting of Psalm 51. This was not allowed to be copied and its performance was banned anywhere else in the world except within the Sistine chapel. Wolfgang heard it while attending service one Wednesday and wrote it down later that day, entirely from memory. On a second hearing on a Good Friday, with the manuscript hidden under his hat, Wolfgang made some minor corrections to it.</p>

<p><a href="/uploads/script/mozart/the-first-opera01.jpg" rel="attachment wp-att-1637"><img alt="the-first-opera01" src="/uploads/script/mozart/the-first-opera01.jpg" width="430" height="632" /></a></p>

<p>Wolfgang gave this manuscript to Dr. Charles Burney, an English music historian who was also in Rome at that time. The doctor went ahead and published the piece. When this news reached the Pope, he summoned Wolfgang. With great trepidation, the young musician complied. But Wolfgang need not have feared. The Pope showered praises on him and instead of excommunicating him, lifted the ban on the <em>‘Miserere’</em>. Also, Wolfgang was conferred with the Order of the Golden Spur, which is a kind of knighthood conferred on people who have contributed to the glory of the church.</p>

<p>During this time, Wolfgang wrote his first major opera<em>, Mitridate re di Ponto</em> or <em>Mithridates,</em> King of Pontus which was performed in Milan. At this time, Mozart’s own voice was breaking. This annoyed him and Leopold reported to Salzburg that Wolfgang could no longer sing the music that he was composing; a composition which was an <em>‘opera seria’</em> and consisted almost entirely of Recitatives and Arias.</p>

</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Opera seria</strong>- A type of opera performed in 18th century Italy based on a serious plot.</p>
<p><strong>Recitatives</strong>- A vocal passage of narrative text in a musical composition.</p>

</div>

<p><a href="/uploads/script/mozart/the-first-opera02.jpg" rel="attachment wp-att-1638"><img class="alignnone size-full wp-image-1638" alt="the-first-opera02" src="/uploads/script/mozart/the-first-opera02.jpg" width="900" height="550" /></a></p>

<div id="biography-script-boxCenter">

<p>Wolfgang had to work closely with the singers to do justice to the opera. The work on the recitatives was exhausting and Mozart complained to his mother that it made his fingers ache. The opera was performed during the Christmas of 1771 and lasted for about six hours. Though it was never revived in Mozart’s lifetime, people liked the opera and it helped him to obtain two more commissions from Milan, the <em>Serenata Ascanio</em> (a pastoral opera in two parts ) in Alba and the opera <em>seria Lucio Silla</em>.</p>

<p>The Serenata Ascanio in Alba was prepared for the marriage of Archduke Ferdinand, one of the sons of Empress Maria Theresia; more aching fingers followed but it was well worth the effort. Mozart’s Serenata completely overshadowed the popular German composer Hasse’s opera series which was also commissioned for the wedding. Leopold joyously reported to Salzburg: <strong>“It really upsets me a lot, but Wolfgang’s serenata has killed the Hasse’s opera.”</strong> Ferdinand was impressed by Wolfgang’s performance and he asked his mother whether he might take the young composer into his employment. But the Empress told him not to <strong>“burden himself with useless people… who go about like beggars… besides he has a large family.”</strong></p>

</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Cantata</strong>- A musical composition for voices and orchestra based on a religious text.</p>
<p><strong>Serenata</strong>- An 18th century dramatic cantata.</p>

</div>

<div id="biography-script-boxCenter">

<p>Flush with the success of <em>Ascanio</em>, the father-son duo returned to Salzburg only to be struck by a grievous blow. The ever obliging Archbishop Schrattenbach had died and a new Prince Archbishop Hieronymus Colloredo was appointed in March 1772. This appointment was to be the source of much annoyance and vexation to the Mozart’s in the years to come. Wolfgang soon became busy composing sacred music, Symphonies, dances and Divertimenti. In October, the duo set off for Milan for the opera seria ‘<em>Lucio Silla’</em> which proved to be even more successful than Mitridate with 26 performances!</p>

</div>

<div style="width: 560px; height: auto; padding: 20px 20px 5px 80px; background: #ebe9e9 url('/uploads/script/mozart/note-icon.jpg') left top no-repeat; margin-left: 90px;">
<p><strong>Divertimenti</strong>- A piece of entertaining music.</p>

</div>














<h1>Life in a cage</h1>
<p><img src="/uploads/script/mozart/mozart-life-in-a-cage.jpg" alt="mozart-life-in-a-cage"></p>

<div id="biography-script-boxRight">
<div style="margin-top: -20px;">
<p>Wolfgang and his father returned to Salzburg from their last trip to Italy in March 1773. The new Archbishop soon appointed Wolfgang as an assistant concert master with a small salary. During this time, the young maestro added greatly to his repertoire of sacred music. However, both, Wolfgang and Leopold were unhappy working for the new Archbishop. Colloredo believed in disciplined worship without too much ceremony which meant that the music part of the service was severely cut short<strong>. </strong>Also, he was against his employees going off to foreign parts, where they made good money and yet they had to be paid as if they were in Salzburg.</p>

<p>Wolfgang also longed to compose operas and Salzburg provided only rare occasions for these. In 1775, the situation worsened when the court theatre was closed. The young composer felt stifled; if only he could break free! His father thought on the same lines that Wolfgang must find a job outside Salzburg. With this in view, in August of 1777, Leopold sought permission from Colloredo for another leave which was promptly rejected. The Archbishop rightly divined that the leave would amount to a job hunting trip and he gave both father and son, the sack. After much wheedling, he agreed to relieve Wolfgang from his services and retain Leopold but only if the latter stayed back in Salzburg.</p>
</div>
</div>

<div style="clear: both;"></div>











<h1>First trip as a free bird</h1>
<p style="padding-left: 30px;"><img src="/uploads/script/mozart/mozart-first-trip-as-afree-bird.jpg" alt="mozart-first-trip-as-afree-bird"></p>

<div id="biography-script-boxCenter">

<p>Leopold decided that his wife must accompany their son on this trip. Accordingly, in September of that year, Wolfgang set off on his first journey without Leopold. The young composer was wildly excited; the feeling of being free was exhilarating. Though his mother accompanied him, she was not demanding like her husband. Leopold made the preparations and planned out a route which the mother-son duo was supposed to follow .This route would bring them in contact with friends along the way who may prove to be very helpful. Though Leopold was not going with Wolfgang, his entire heart was on the trip and he meant to closely monitor the entire journey trip from home. Wolfgang had strict instructions from his father: either find a good job or make money.</p>

<p>Their first stop was in Munich. Young Wolfgang followed Leopold’s strategy which was to introduce oneself to people in high places, give a concert in the hope of receiving commissions and teach and compose for the local populace. But the strategy did not pay off as Munich declined politely. The duo then moved on to Augsburg, where Wolfgang spent much of his time with his cousin Maria Anna Thekla Mozart ( nicknamed Basle). Some believe that the cousins were romantically involved, perhaps because of the amorous letters which Wolfgang wrote to Basle about moving away from Augsburg. As far as his music was concerned, Wolfgang gave just one concert here but there was no court in Augsburg to employ him. By the end of October, mother and son were in Mannheim. This stop proved to be crucial in many ways and it sowed the seeds of friction between Wolfgang and his father.</p>

</div>












