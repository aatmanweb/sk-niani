<div id="biography-script-boxCenter">
<p>But it was not all play; Wolfgang worked very hard as well. The composer in him was very busy in Mannheim. He became interested in wind instruments and very soon became the most adroit composer for wind instruments of his time. He disliked the flute though and when he received a commission from a skilled amateur flute player for some music involving flute he did not finish the work and received only 96 gulden (florins) instead of the promised 200. It took many days for his father to forgive him for this sin.</p>

<p>Wolfgang was delighted with Mannheim. He knew that he would need to convince Leopold about his prospects in the town if he was to stay there for long. So, he would send weekly bulletins to his father writing about his successes in Mannheim.</p>

<p><strong>“The Elector, the Electress and the entire court are well pleased with me. In the Academie, the two times I performed, both he and she came right up to me and stood next to me at the Clavier… I talked with the Elector as if he and I were the best of friends… The Elector himself asked again and again; he sat right next to me and sat perfectly still.”</strong></p>

<p>It was in Mannheim that Wolfgang met the family of Fridolin Weber. The family was far from wealthy; Weber was an occasional singer, prompter and copyist in the theatre. Wolfgang was directed to Weber as he would be able to copy music for him. Weber had four daughters who were all talented musicians. The young Wolfgang might have hoped that he would appeal to them as he was not bad looking. Very soon, he fell in love with the second daughter Aloysia. Though his increasingly frustrated father directed him to find a steady job or else go someplace where money could be made, Wolfgang continued to stay in Mannheim, enjoying Aloysia’s company and composing music. In early 1778, the two of them concocted a plan to go to Italy to find Aloysia some singing engagements. Wolfgang boldly told his father of his plans.</p>

<p>Leopold was filled with rage as he knew he would lose control on his son once he got married; also he justly saw that the plan was nonsensical. He wrote a furious letter to his son: <strong>“I have read your letter of February 4 with amazement and horror… Your proposal to travel about with Herr Weber and, Nota Bene, 2 of his daughters has almost driven me to insanity… Off with you to Paris!”</strong> Leopold held the purse strings in the family and so, soon succeeded in packing off his son and wife to Paris.</p>

</div>
















<h1>Tragedy strikes in Paris</h1>
<div id="biography-script-boxCenter">
<p><img src="/uploads/script/mozart/mozart-tragedy-strikes-in-paris.jpg" alt="mozart-tragedy-strikes-in-paris"></p>

<p>Paris proved to be a failure right from the start. The main issue was, Paris was already habituated with musicians from various nationalities. So for them, Wolfgang was no one special but just one of the many brilliant composers that it was used to hearing. The young Wolfgang felt it sorely and wrote to his father <strong>“these stupid Frenchmen think I am just 7 years old… they treat me here like a beginner – except the musicians; they know better. But it’s the general public that counts.” </strong>Wolfgang got no commissions to write full length operas, only a short ballet ‘<em>Les petit riens’</em> or <em>A bit of nothing</em>, which was one of his least momentous compositions. He tried his luck as a teacher and made some headway but the cost of travelling to his pupils’ homes sounded the death knell for this venture. The only success of his visit was the Symphony ‘<em>Paris</em>’ which he composed. A well-wisher of theirs told both father and son, that Wolfgang was too friendly and laid back to make a mark in Paris.</p>

<p>Meanwhile, Wolfgang and his mother were starting to run out of money and Wolfgang had to pawn several of his personal belongings to pay for their living and travelling expenses. To make matters worse, his mother fell ill in July 1778 with fever. There was some delay in procuring medical assistance either because of his mother’s insistence for a German doctor or their acute shortage of funds. Finally, a doctor came but he couldn’t save Wolfgang’s mother and she died on 3rd July. Wolfgang was devastated and all alone in an alien place with only himself for support. He wrote one of his gloomiest yet greatest piano Sonatas K.310 in ‘A minor’ as a result of his mother’s death.</p>

